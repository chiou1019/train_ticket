def resolve_station(location_text: str):
    """
    回傳 (站名, OD_ID, FULL_ID) 或 None
    """
    if not location_text:
        return None
    text = location_text.strip()
    if text in KEYWORD_TO_STATION:
        return KEYWORD_TO_STATION[text]
    sorted_keys = sorted(KEYWORD_TO_STATION.keys(), key=len, reverse=True)
    for keyword in sorted_keys:
        if keyword in text:
            return KEYWORD_TO_STATION[keyword]
    return None
# 台鐵官網用「臺」不是「台」，爬蟲選站時需要轉換
TW_NAME_MAP = {
    "台北": "臺北",
    "台中": "臺中",
    "台南": "臺南",
    "台東": "臺東",
}

def to_official_name(name: str) -> str:
    """把站名轉成台鐵官網格式"""
    return TW_NAME_MAP.get(name, name)

